def y_bar(t):
    A1 = (2*t)*(105)
    A2 = 2*(65-4*(t))*(t)
    A3 = (2*t)*(70)
    A4 = 2*(10)*(t)

    y1 = (65-t)
    y2 = 33.77
    y3 = (t)
    y4 = 61.825

    y_bar = ((A1*y1) + (A2*y2) + (A3*y3) + (A4*y4))/(A1+A2+A3+A4)

    return y_bar

